
public class DoWhileLoop {

	public static void main(String[] args) {

		int number = 1;
		System.out.println("Using do-while loop:");
		do {
			System.out.println("Current number is: " + number);
			number++;
		} while (number<=5);
		
			System.out.println("Loop has ended. Final number: " + number);
	}

}
